// ===== 年柱テーブル構築 =====
function build_yyyy_table($Kyusei9, $Seido60) {
    $yyyy_table = [];
    $yyyy    = YYYY_TBL_START;
    $eto60   = YYYY_ETO60_START;
    $kyusei  = YYYY_KYUSEI_START;

    $count = YYYY_TBL_END - YYYY_TBL_START + 1;
    for ($i = 0; $i < $count; $i++) {
        $row = [];
        $row['yyyy']  = $yyyy;
        $row['uruu']  = ($yyyy % 4 == 0) ? 1 : 0;

        // 元号
        if ($yyyy >= 1912 && $yyyy < 1926) {
            $row['gengou'] = 'T'; $row['gengou_yy'] = $yyyy - 1912 + 1;
        } elseif ($yyyy >= 1926 && $yyyy < 1989) {
            $row['gengou'] = 'S'; $row['gengou_yy'] = $yyyy - 1926 + 1;
        } elseif ($yyyy >= 1989 && $yyyy < 2019) {
            $row['gengou'] = 'H'; $row['gengou_yy'] = $yyyy - 1989 + 1;
        } else {
            $row['gengou'] = 'R'; $row['gengou_yy'] = $yyyy - 2019 + 1;
        }

        // 小寒日(開始日)
        $row['mm'] = 1;
        $row['dd'] = 6;
        if ($yyyy >= 1952 && ($yyyy % 4) == 1) $row['dd'] = 5;
        if ($yyyy >= 1992 && ($yyyy % 4) == 2) $row['dd'] = 5;
        if ($yyyy >= 2032 && ($yyyy % 4) == 3) $row['dd'] = 5;

        // 立春日
        $row['risyun_mm'] = 2;
        $D = 4.8693; $A = 0.242713;
        $yy = ($yyyy - 1) - 1900;
        $row['risyun_dd'] = (int)($D + $A * $yy) - (int)($yy / 4);

        // 干支No
        $row['eto60_no'] = $eto60;
        $eto60--;
        if ($eto60 == -1) $eto60 = YYYY_ETO60_START;

        // 九星No
        $row['kyusei_no'] = $kyusei;
        $kyusei--;
        if ($kyusei == 0) $kyusei = 9;

        $yyyy_table[] = $row;
        $yyyy++;
    }
    return $yyyy_table;
}

// ===== 月柱・日柱テーブル構築 =====
function build_mmdd_table($yyyy_table, $Eto_Zoukan, $Nichimei, $Zoukan12) {
    $mmdd_table = [];
    $yyyy    = YYYY_TBL_START;
    $eto60_mm = YYYY_ETO60_START;
    $ez_count = 0;
    $nm_count = 0;
    $eto60_dd = DD_SEIDO60_ETO;
    $youbi_count = MM_YOUBI_START;

    $count = YYYY_TBL_END - YYYY_TBL_START + 1;
    for ($i = 0; $i < $count; $i++) {
        for ($mm = 1; $mm <= 12; $mm++) {
            $row = [];
            $row['yyyy']     = $yyyy;
            $row['mm']       = $mm;
            $row['eto60_no'] = $eto60_mm;
            $eto60_mm--;
            if ($eto60_mm == -1) $eto60_mm = YYYY_ETO60_START;

            $row['zoukan_no'] = $Zoukan12[$mm - 1];

            $row['eto60_dd']  = $Eto_Zoukan[$ez_count][0];
            $row['zoukan_dd'] = $Eto_Zoukan[$ez_count][1];
            $ez_count++;

            $row['nichi_mei'] = $Nichimei[$nm_count];
            $nm_count++;
            if ($nm_count >= 144) $nm_count = 0;

            // 日柱 nichi_cyu
            $row['nichi_cyu'] = $eto60_dd + 1;
            $dd_end = month_days_calc($yyyy, $mm);
            for ($d = 0; $d < $dd_end; $d++) {
                $eto60_dd--;
                if ($eto60_dd == -1) $eto60_dd = YYYY_ETO60_START;
            }

            // 曜日
            $row['youbi'] = $youbi_count % 7;
            $youbi_count += $dd_end;

            $mmdd_table[] = $row;
        }
        $yyyy++;
    }
    return $mmdd_table;
}

function month_days_calc($yyyy, $mm) {
    $uruu = ($yyyy % 4 == 0) ? 1 : 0;
    switch ($mm) {
        case 1: case 3: case 5: case 7: case 8: case 10: case 12: return 31;
        case 2: return $uruu ? 29 : 28;
        default: return 30;
    }
}

