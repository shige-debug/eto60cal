    return null;
}

// ===== 干支カレンダー計算 =====
function calc_eto60cal($yyyy, $yyyy_table, $mmdd_table, $Seido60, $Kyusei9) {
    $cal = [];

    // 年柱の干支・九星
    $yy_row = find_yyyy_row($yyyy_table, $yyyy);
    if (!$yy_row) return null;

    // 今日の情報(年回り)
    $today = getdate();
    $today_yyyy = $today['year'];
    $today_mm   = $today['mon'];
    $today_dd   = $today['mday'];

    // 年の干支(立春基準)
    $yy_idx = $yyyy;
    $risyun_row = find_yyyy_row($yyyy_table, $yyyy);

    $yy_eto_row = $yy_row;
    // 年柱干支の取得
    $yy_seido60 = find_seido60($Seido60, $yy_row['eto60_no']);
    $yy_kyusei  = find_kyusei9($Kyusei9, $yy_row['kyusei_no']);

    $cal['yy_eto60_name']   = $yy_seido60 ? $yy_seido60['eto60_name'] : '';
    $cal['yy_kyusei_no']    = $yy_row['kyusei_no'];
    $cal['yy_kyusei_name_no'] = $yy_kyusei ? $yy_kyusei['name_no'] : '';

    // 12ヶ月分のカレンダー計算
    for ($mm = 1; $mm <= 12; $mm++) {
        $mm_row = find_mmdd_row($mmdd_table, $yyyy, $mm);
        if (!$mm_row) continue;

        // 月柱干支
        $mm_seido60 = find_seido60($Seido60, $mm_row['eto60_no']);
        $cal['months'][$mm]['eto60_name'] = $mm_seido60 ? $mm_seido60['eto60_name'] : '';
        $cal['months'][$mm]['eto60_dd']   = $mm_row['eto60_dd'];

        $days = month_days_calc($yyyy, $mm);
        $youbi = $mm_row['youbi'];

        for ($dd = 1; $dd <= $days; $dd++) {
            // 日柱の干支
            $nichi_cyu = $mm_row['nichi_cyu'];
            if ($nichi_cyu < $dd) $nichi_cyu += 60;
            $dd_eto60_no = $nichi_cyu - $dd;
            $dd_seido60 = find_seido60($Seido60, $dd_eto60_no);

            // 日柱の九星
            $kyusei_no = calc_dd_kyusei($mm_row['nichi_mei'], $dd);
            $dd_kyusei = find_kyusei9($Kyusei9, $kyusei_no);

            $cal['months'][$mm]['days'][$dd] = [
                'youbi'          => $youbi % 7,
                'youbi_name'     => youbi_name($youbi % 7),
                'eto60_no'       => $dd_eto60_no,
                'eto60_name'     => $dd_seido60 ? $dd_seido60['eto60_name'] : '',
                'kyusei_no'      => $kyusei_no,
                'kyusei_name_no' => $dd_kyusei ? $dd_kyusei['name_no'] : '',
            ];
            $youbi++;
        }
    }
    return $cal;
}

// ===== 日の九星計算 =====
function calc_dd_kyusei($nichimei, $dd) {
    $sub = $nichimei - $dd;

    $msb = (int)($nichimei / 10);
    $lsb = $nichimei % 10;
    $tansuu_nichimei = $msb + $lsb;

    $msb = (int)($dd / 10);
    $lsb = $dd % 10;
    $tansuu_dd = $msb + $lsb;
    if ($tansuu_dd > 9) {
        $msb = (int)($tansuu_dd / 10);
        $lsb = $tansuu_dd % 10;
        $tansuu_dd = $msb + $lsb;
    }

    if ($sub > 9) {
        $kyusei_no = $tansuu_nichimei - $dd;
    } elseif ($sub < 1) {
        $tansuu_sub = $nichimei - $tansuu_dd;
        if ($tansuu_sub >= 1 && $tansuu_sub < 10) {
            $kyusei_no = $tansuu_sub;
        } else {
            $kyusei_no = $tansuu_nichimei - $tansuu_dd;
        }
    } else {
        $kyusei_no = $sub;
    }

    return $kyusei_no;
}

// ===== 曜日名 =====
function youbi_name($n) {
    $names = ['日','月','火','水','木','金','土'];
    return $names[$n % 7];
}

